function [S] = txt_update
    % Creates a textbox.
    S.fh = figure('units','pixels',...
        'position',[40 40 240 940],...
        'menubar','none',...
        'resize','off',...
        'numbertitle','off',...
        'name','Program output');

    S.tx = uicontrol('style','edit',...
        'position',[6 2 550 410],...
        'String', {},...
        'backgroundcolor','w',...
        'HorizontalAlign','left',...
        'min',0,'max',10,...
        'enable','inactive');
    set(S.tx, 'Units', 'normalized');
end